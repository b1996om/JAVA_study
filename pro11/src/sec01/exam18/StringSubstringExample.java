package sec01.exam18;
// String 클래스
// 문자열 잘라내기 - substring() :주어진 인덱스에서 문자열을 추출
public class StringSubstringExample {
	public static void main(String[] args) {
		String ssn = "880815-1234567";
	
		String firstNum = ssn.substring(0, 6); //인덱스 0부터 6전까지(5).
		System.out.println(firstNum);  //880815
		
		String secondNum = ssn.substring(7); //인덱스 7부터 끝까지.
		System.out.println(secondNum);  //1234567
	}

}
